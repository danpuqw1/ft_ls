#include "libft.h"

char	*ft_strchr(const char *str, int symbol)
{
	int		i;
	char	*s;

	s = (char*)str;
	i = 0;
	while (s[i] != '\0' && s[i] != symbol)
		i++;
	if (s[i] == '\0' && symbol != '\0')
		return (0);
	return (s + i);
}
